// Addition
function add(){
    var num1 = 9
    var num2 = 3
    var answer = num1+num2;
    console.log(num1 + " + " + num2 + " = " + answer)
}
add();

//Subtraction
function subtract(){
    var num1 = 9
    var num2 = 3
    var answer = num1-num2;
    console.log(num1 + " - " + num2 + " = " + answer)
}
subtract();

//Multiplication
function multiply(){
    var num1 = 9
    var num2 = 3
    var answer = num1*num2;
    console.log(num1 + " * " + num2 + " = " + answer)
}
multiply();

